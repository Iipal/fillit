#include "includes/libft.h"

char	*ft_strstr(const char *str, const char *to_find)
{
	char *cp;
	char *s1;
	char *s2;

	cp = (char*)str;
	if (!*to_find)
		return ((char*)str);
	while (*cp)
	{
		s1 = cp;
		s2 = (char*)to_find;
		while (*s1 && *s2 && !(*s1 - *s2))
		{
			s1++;
			s2++;
		}
		if (!*s2)
			return (cp);
		cp++;
	}
	return (0);
}
