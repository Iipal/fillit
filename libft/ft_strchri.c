#include "includes/libft.h"

size_t	ft_strchri(const char *s, int c)
{
	size_t	i;
	char	ch;

	ch = c;
	i = -1;
	while (s[++i] != '\0')
		if (s[i] == ch)
			return (i);
	return (-1);
}
